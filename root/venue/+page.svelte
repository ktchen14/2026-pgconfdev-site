<h2>Travel</h2>
<p>
  Vancouver International Airport (YVR) is the closest airport and has flights
  to many international destinations.
</p>

<h4>Canada Line from YVR to the Conference</h4>
<p>
  The best way to get from Vancouver International Airport to the Conference is
  to take the <a href="http://thecanadaline.com">Canada Line(Skytrain)</a> from the
  airport to Waterfront station. The Harbour Centre is directly across the sreet
  from the Waterfront train station
</p>

<h2>Visa Information</h2>
<p>
  Travellers from many countries need either a vistor visa or an Electronic
  Travel Authorization (eTA). You can use
  <a href="https://ircc.canada.ca/english/visit/visas.asp">this tool</a> to determine
  which applies to you.
</p>

<h3>U.S. Citizens or permanent residents</h3>
<p>
  U.S. Citizen must have a valid passport to enter Canada. U.S. lawful permanent
  residents must have the documents described
  <a
    href="https://www.canada.ca/en/immigration-refugees-citizenship/services/visit-canada/entry-requirements-country.html#lawful-pr-us"
    >here</a
  >
</p>

<h3>Electronic Travel Authorization(eTA)</h3>
<p>
  Visitors from <a href="https://ircc.canada.ca/english/visit/visas.asp"
    >visa-exempt countries</a
  >
  need to apply for an eTa. The
  <a
    href="https://www.canada.ca/en/immigration-refugees-citizenship/services/visit-canada/eta/apply.html"
  >
    eTa site</a
  > contains instructions for applying for an eTa. There is a fee for this but most
  applications are approved quickly.
</p>

<h3>Visitor visa</h3>
<p>
  Procedures for obtaining a visitor visa are available from
  <a
    href="https://www.canada.ca/en/immigration-refugees-citizenship/services/visit-canada/about-visitor-visa.html"
  >
    What is a visitor visa</a
  >. This process can be lengthy and we advise that you start it as soon as you
  have decided that you will be attending the conference.
</p>

